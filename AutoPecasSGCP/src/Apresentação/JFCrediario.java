    private void jBAbrirCrediarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAbrirCrediarioActionPerformed
        // TODO add your handling code here:
        
        Crediario crediario = new Crediario();
        
        crediario.setValor(Double.parseDouble(jTFValorCrediario.getText()));
        crediario.setSituacao(jRSituacao.isSelected() == true ? "Quitado" : "Não Quitado");
        
        Cliente cliente = null;
        String busca = (String) jCBCliente.getSelectedItem();
        for (Cliente c: clientes){
            if(busca.equals(c.getNome()))
                cliente = c;
        }
        crediario.setCliente(cliente);
        
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        try {
            Date data = formato.parse(jFTFDataCrediario.getText());
            crediario.setDataDeAbertura(data);
        } catch (ParseException ex) {
            Logger.getLogger(JFCompra.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(jTFCrediarioCodigo.getText().equals("")){
            CrediarioDAO.getInstance().persist(crediario);
            
            int cont = jTCrediario.getRowCount();

            for(int i = 0; i < cont; i++){
                Parcela parcela = new Parcela();
                parcela.setDiaUtilBom((String) jTCrediario.getValueAt(i, 0));
                parcela.setValorParcelado((Double) jTCrediario.getValueAt(i, 1));
                parcela.setSituacao((String) jTCrediario.getValueAt(i, 2));
                parcela.setCrediario(crediario);
                ParcelaDAO.getInstance().persist(parcela);
            }
        }else{           
            crediario.setCodigo(Integer.parseInt(jTFCrediarioCodigo.getText()));
            CrediarioDAO.getInstance().merge(crediario);
            
        }
        
        limpar();
        JOptionPane.showMessageDialog(this, " Crediário registrado com sucesso!");
        
        new JFPagamento(crediario).setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jBAbrirCrediarioActionPerformed
    
