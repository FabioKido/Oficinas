    public void preencherComboBox(){
        jCBCliente.removeAllItems();
        String nome = jTFNomeCliente.getText();
        for(Cliente c: clientes){
            if(c.getNome().toUpperCase().startsWith(nome.toUpperCase()))
            jCBCliente.addItem(c.getNome());
        }
    }
    
    private void limpar(){
        jTFCrediarioCodigo.setText("");
        jTFValorCrediario.setText("");
        jTFValorParcela.setText("");
        DefaultTableModel dtm = (DefaultTableModel) jTCrediario.getModel();
        dtm.setNumRows(0);
    }    
        
