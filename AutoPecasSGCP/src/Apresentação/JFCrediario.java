    private void jBAdicionarParcelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAdicionarParcelaActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel dtm = (DefaultTableModel) jTCrediario.getModel();
        
        int cont = 0;
        Double parcela = Double.parseDouble(jTFValorParcela.getText());
        
        for(int i=0; i < cont; i++){
            dtm.setNumRows(0);
        }
        
        dtm.addRow(new Object[]{"informe",parcela,"informe"});
        
    }//GEN-LAST:event_jBAdicionarParcelaActionPerformed

    private void jBRemoverParcelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRemoverParcelaActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel dtm = (DefaultTableModel)jTCrediario.getModel();
        if (jTCrediario.getSelectedRow() >= 0){
            dtm.removeRow(jTCrediario.getSelectedRow());
            jTCrediario.setModel(dtm);
        }else{
            JOptionPane.showMessageDialog(null, "Favor selecionar uma linha");
        }
    }//GEN-LAST:event_jBRemoverParcelaActionPerformed

