    public JFCrediario(Crediario c){
        
        initComponents();
        formatarData();
        jRSituacao.setEnabled(true);        
        jCBCliente.setEnabled(false);
        jTFNomeCliente.setEnabled(false);
        
        jTFCrediarioCodigo.setText(String.valueOf(c.getCodigo()));
        jTFValorCrediario.setText(String.valueOf(c.getValor()));
        jTFNomeCliente.setText(c.getCliente().getNome());
        jCBCliente.removeAllItems();
        jCBCliente.addItem(jTFNomeCliente.getText());
        
        formatarData();
        
        DefaultTableModel dtm = (DefaultTableModel) jTCrediario.getModel();
        
        List<Parcela> parcelas = ParcelaDAO.getInstance().findAll();
        for(Parcela p: parcelas){
            if(p.getCrediario().getCodigo() == c.getCodigo()){
                dtm.addRow(new Object[]{p.getDiaUtilBom(),p.getValorParcelado(),p.getSituacao()});
            }
        }
    }
    
