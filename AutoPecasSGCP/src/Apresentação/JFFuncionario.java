    private void jBSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSalvarActionPerformed
        // TODO add your handling code here:

        Funcionario funcionario = new Funcionario();

        funcionario.setNome(jTFNome.getText());
        funcionario.setFuncao(jTFFuncao.getText());
        funcionario.setRg(jTFRg.getText());
        funcionario.setCpf(jTFCpf.getText());
        funcionario.setEndereco(jTFEndereco.getText());
        funcionario.setTelefone(jTFTelefone.getText());

        if(jTFCodigo.getText().equals("")){
            FuncionarioDAO.getInstance().persist(funcionario);
        }else{
            funcionario.setCodigo(Integer.parseInt(jTFCodigo.getText()));
            FuncionarioDAO.getInstance().merge(funcionario);
        }

        limpar();
        JOptionPane.showMessageDialog(this, " Salvo com sucesso!");
    }//GEN-LAST:event_jBSalvarActionPerformed

