    private void somarValorTotal(){
        
        Double valor, soma = 0.0;
        
        int cont = jTVendas.getRowCount();
        
        for(int i = 0; i < cont; i++){
            valor = (Double) jTVendas.getValueAt(i, 4);
            soma += valor;
        }
        
        jTFValorTotal.setText(String.valueOf(soma));
    }
    
    private void jBAdicionarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAdicionarItemActionPerformed
        // TODO add your handling code here:
        Mercadoria mercadoria = null;
        String busca = (String) jCBMercadoria.getSelectedItem();
        for (Mercadoria m: mercadorias){
            if(busca.equals(m.getDescricao()))
                mercadoria = m;
        }
        
        DefaultTableModel dtm = (DefaultTableModel) jTVendas.getModel();
        
        int cont = 0;
        Double quantidade = 0.0;
        quantidade = Double.parseDouble(jTFQuantidade.getText());
        int qtd = Integer.parseInt(jTFQuantidade.getText());
        
        for(int i=0; i < cont; i++){
            dtm.setNumRows(0);
        }
        
        dtm.addRow(new Object[]{mercadoria.getCodigo(),mercadoria.getDescricao(),mercadoria.getValorUnitario(),qtd,quantidade * mercadoria.getValorUnitario()});
        
        somarValorTotal();
    }//GEN-LAST:event_jBAdicionarItemActionPerformed

