    public void preencherComboBox(){
        jCBMercadoria.removeAllItems();
        String descricao = jTFDescricao.getText();
        for(Mercadoria m: mercadorias){
            if(m.getDescricao().toUpperCase().startsWith(descricao.toUpperCase()))
            jCBMercadoria.addItem(m.getDescricao());
        }
    }
    
    private void limpar(){
        jTFVendaCodigo.setText("");
        jTFDescricao.setText("");
        jTFQuantidade.setText("");
        jTFValorTotal.setText("");
        DefaultTableModel dtm = (DefaultTableModel) jTVendas.getModel();
        dtm.setNumRows(0);
    }
    
