    private void formatarData(){
        try{
            MaskFormatter mask = new MaskFormatter("##/##/####");
            mask.install(jFTFDataVenda);
        }catch(ParseException ex){
            JOptionPane.showMessageDialog(null, "Erro ao Formatar Campo", "ERRO", JOptionPane.ERROR);
        }
    }
    
    private void jBFinalizarVendasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBFinalizarVendasActionPerformed
        // TODO add your handling code here:
        
        Vendas vendas = new Vendas();
        ItemVenda itemVenda = new ItemVenda();
        
        vendas.setValorTotal(Double.parseDouble(jTFValorTotal.getText()));
        vendas.setSituacao(jRSituacao.isSelected() == true ? "Cancelada" : "Confirmada");
                
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        try {
            Date data = formato.parse(jFTFDataVenda.getText());
            vendas.setDataDeVenda(data);
        } catch (ParseException ex) {
            Logger.getLogger(JFCompra.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(jTFVendaCodigo.getText().equals("")){
            VendasDAO.getInstance().persist(vendas);
            
                    int cont = jTVendas.getRowCount();
                    Mercadoria mercadoria = new Mercadoria(); 

                    for(int i = 0; i < cont; i++){
                        itemVenda = new ItemVenda();
                        mercadoria = new Mercadoria();
                        mercadoria = MercadoriaDAO.getInstance().getById((int) jTVendas.getValueAt(i, 0));
                        itemVenda.setMercadoria(mercadoria);
                        itemVenda.setVendas(vendas);
                        itemVenda.setQuantidade((int) jTVendas.getValueAt(i, 3));
                        ItemVendaDAO.getInstance().persist(itemVenda);
                    }
        }else{           
            vendas.setCodigo(Integer.parseInt(jTFVendaCodigo.getText()));
            VendasDAO.getInstance().merge(vendas);
        }
        
        limpar();
        JOptionPane.showMessageDialog(this, " Venda realizada com sucesso!");
        
    }//GEN-LAST:event_jBFinalizarVendasActionPerformed

    private void jBRemoverItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRemoverItemActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel dtm = (DefaultTableModel)jTVendas.getModel();
        if (jTVendas.getSelectedRow() >= 0){
            dtm.removeRow(jTVendas.getSelectedRow());
            jTVendas.setModel(dtm);
        }else{
            JOptionPane.showMessageDialog(null, "Favor selecionar uma linha");
        }
        
        somarValorTotal();
    }//GEN-LAST:event_jBRemoverItemActionPerformed

