    private void jBRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRegistrarActionPerformed
        // TODO add your handling code here:
        
        Compra compra = new Compra();
        
        compra.setQuantidade(Integer.parseInt(jTFQuantidade.getText()));
        compra.setPrecoTotal(Double.parseDouble(jTFPrecoTotal.getText()));
        Mercadoria mercadoria = MercadoriaDAO.getInstance().getById(Integer.parseInt(jTFMercadoria.getText())); // Try e Catch aqui e no outro!!!
        compra.setMercadoria(mercadoria);
        
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        try {
            Date data = formato.parse(jFTFDataCompra.getText());
            compra.setDataDeCompra(data);
        } catch (ParseException ex) {
            Logger.getLogger(JFCompra.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(jTFCodigo.getText().equals("")){
            CompraDAO.getInstance().persist(compra);
        }else{
            
        }

        limpar();
        JOptionPane.showMessageDialog(this, " Salvo com sucesso!");
    }//GEN-LAST:event_jBRegistrarActionPerformed

