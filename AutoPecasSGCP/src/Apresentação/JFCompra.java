    private void formatarData(){
        try{
            MaskFormatter mask = new MaskFormatter("##/##/####");
            mask.install(jFTFDataCompra);
        }catch(ParseException ex){
            JOptionPane.showMessageDialog(null, "Erro ao Formatar Campo", "ERRO", JOptionPane.ERROR);
        }
    }
    
    private void limpar(){
        jTFCodigo.setText("");
        jTFQuantidade.setText("");
        jFTFDataCompra.setText("");
        jTFMercadoria.setText("");
        jTFDescricaoMercadoria.setText("");
        jTFPrecoTotal.setText("");
    }
    
