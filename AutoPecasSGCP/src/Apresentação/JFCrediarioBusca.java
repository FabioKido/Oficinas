    private void listaCrediarios(){
            
        DefaultTableModel dtm = (DefaultTableModel) jTCrediarios.getModel();
        
        ((DefaultTableModel) jTCrediarios.getModel()).setNumRows(0);
        jTCrediarios.updateUI();
        
        String situacao = jTFSituacao.getText();
        for(Crediario c: crediarios){
            if(c.getSituacao().toUpperCase().startsWith(situacao.toUpperCase()))
                dtm.addRow(new Object[]{c.getCodigo(),c.getCliente().getNome(),c.getValor(),c.getSituacao()});
        }
            
    }
    
