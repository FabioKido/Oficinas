    private void jBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBuscarActionPerformed
        // TODO add your handling code here:

        Crediario crediario = null;
        int linha = jTCrediarios.getSelectedRow();
        System.out.println(linha);

        String busca = (String) jTCrediarios.getValueAt(linha, 3);
        for (Crediario c: crediarios){
            if(busca.equals(c.getSituacao()))
            crediario = c;
        }
        if(crediario!=null){
            new JFCrediario(crediario).setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Crediário não encontrado");
        }
    }//GEN-LAST:event_jBuscarActionPerformed

