    private void jBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBuscarActionPerformed
        // TODO add your handling code here:
        Funcionario funcionario = null;
        int linha = jTFuncionarios.getSelectedRow();
        System.out.println(linha);
        String busca = (String) jTFuncionarios.getValueAt(linha, 1);
        for (Funcionario c: funcionarios){
            if(busca.equals(c.getNome()))
                funcionario = c;
        }
        if(funcionario!=null){
            new JFFuncionario(funcionario).setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Funcionario não encontrado");
        }
    }//GEN-LAST:event_jBuscarActionPerformed

