    private void listaFuncionarios(){

        DefaultTableModel dtm = (DefaultTableModel) jTFuncionarios.getModel();
        
        ((DefaultTableModel) jTFuncionarios.getModel()).setNumRows(0);
        jTFuncionarios.updateUI();
        
        String nome = jTFNome.getText();
        for(Funcionario c: funcionarios){
            if(c.getNome().toUpperCase().startsWith(nome.toUpperCase()))
                dtm.addRow(new Object[]{c.getCodigo(),c.getNome()});
        }
    }
    
